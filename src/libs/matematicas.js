/**
 * Suma dos numero
 * @param {number} x Primer numero
 * @param {number} y Segundo numero
 * @returns {number} suma de x + y
 */

const mas = (x, y) => x + y;

/**
 * Resta de dos numeros
 * @param {number} x Primer numero
 * @param {number} y Segundo numero
 * @returns {number} resta de x - y
 */
const resta = (x, y) => x - y;
